                            <h3 class="title-article after-line">Что ещё можно сделать?</h3>

                            <p>
                                При борьбе с бесплодием большую роль играют мероприятия, не связанные с приемом лекарств
                                или витаминных комплексов.
                            </p>
                            <ul class="list-article">
                                <li>
                                    Отказ от вредных привычек. <br>
                                    Курение приводит к повышению окислительного стресса, усиливает негативное влияние
                                    варикоцеле на сперматогенез. Отказ от курения может положительно сказаться на
                                    репродуктивной функции мужчины. <br>
                                    Что касается алкоголя, его чрезмерное потребление часто сопровождается такими
                                    нарушениями, как гипогонадизм (функциональная недостаточность яичек) и
                                    субфертильность (пониженная плодовитость). <br>
                                    Употребление наркотических препаратов ведет к гиперпролактинемии. Это гормональное
                                    нарушение, заключающееся в повышенном содержании гормона пролактина. При данном
                                    заболевании могут увеличиться молочные железы, возникнуть проблемы с эрекцией,
                                    нарушиться выработка сперматозоидов. Не менее важно, что при употреблении наркотиков
                                    возникает андрогенный дефицит (недостаток мужских гормонов).
                                </li>
                                <li>
                                    Отказ от тесных плавок, посещения бань, саун, парных, горячих ванн. Все, что ведет к
                                    перегреванию яичек и мошонки, понижает оплодотворяющую способность сперматозоидов.
                                </li>
                                <li>
                                    Умеренные регулярные физические нагрузки. <br> Здесь важно не переусердствовать: с
                                    одной
                                    стороны, умеренные нагрузки оказывают тонизирующее действие на половую систему
                                    мужчины в целом, с другой – долгие и изнурительные процедуры могут негативно
                                    сказаться на репродуктивной способности.
                                </li>
                                <li>
                                    Соблюдение регулярности половых контактов. Подвижные сперматозоиды созревают в
                                    течение 72 часов, поэтому половые контакты должны происходить не чаще одного раза в
                                    4 дня. Иначе количество неподвижных сперматозоидов будет увеличиваться, а шансы на
                                    зачатие – падать.
                                </li>
                                <li>
                                    Сведение к минимуму стрессовых ситуаций.
                                </li>
                                <li>
                                    Коррекция веса. Избыточный вес вызывает гормональные и метаболические нарушения, что
                                    напрямую влияет на мужскую фертильность. Поэтому мужчинам с бесплодием желательно
                                    придерживаться принципов правильного питания и постепенно снизить массу тела.
                                </li>
                            </ul>
