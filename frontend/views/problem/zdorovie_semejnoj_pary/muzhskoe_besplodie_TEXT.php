                                    сперматогенного эпителия под воздействием радиации. Но как правило, невозможно
                                    случайно оказаться под воздействием повышенных доз радиации и не знать об этом. На
                                    предприятиях атомной энергетики или в других учреждениях, где существует риск
                                    подвергнуться радиоактивному облучению, соблюдаются строгие меры защиты, личной
                                    безопасности и ведется дозиметрический контроль, в том числе и индивидуальный.
                                    <br>
                                    Иногда причины бесплодия не могут быть установлены. Такое бесплодие называется
                                    идиопатическим.
                                </p>
