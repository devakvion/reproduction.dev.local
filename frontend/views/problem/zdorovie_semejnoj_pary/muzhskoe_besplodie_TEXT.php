                                <h3 class="title-article after-line">Симптомы и причины</h3>

                                <p>
                                    Симптомы мужского бесплодия отсутствуют, поэтому часто мужчина узнает о снижении
                                    своей фертильности только после обследования. Тем не менее есть ряд косвенных
                                    признаков, которые могут указывать на проблему. Это уменьшение количества спермы,
                                    ухудшение потенции, болевые ощущения.
                                    Мужское бесплодие возникает либо из-за нарушения и ухудшения процессов образования и
                                    созревания сперматозоидов, либо из-за уменьшения оплодотворяющей способности
                                    сперматозоидов.
                                </p>

                                <p>
                                    <b>Рассмотрим причины, которые могут приводить к мужскому бесплодию:
                                    </b>
                                </p>
                                <ul class="list-article">
                                    <li>
                                        Инфекционно-воспалительные, генетические и другие заболевания, а также
                                        последствия этих заболеваний.
                                    </li>
                                    <li>
                                        Гормональная недостаточность.
                                    </li>
                                    <li>
                                        Варикоцеле (расширение вен семенного канатика).
                                    </li>
                                    <li>
                                        Проблемы с иммунной системой.
                                    </li>
                                    <li>
                                        Проблемы с потенцией и/или эякуляцией.
                                    </li>
                                    <li>Прием некоторых лекарственных средств и наркотиков. Уменьшить количество
                                        жизнеспособных подвижных сперматозоидов могут анаболические стероиды, аспирин,
                                        некоторые лекарства от эпилепсии, антидепрессанты, марихуана, кокаин и пр.); а
                                        также радио- или- химиотерапия. Кроме того, на количестве и подвижности
                                        сперматозоидов может временно сказаться перенесенная простуда, проходившая с
                                        повышенной температурой.

                                    </li>
                                    <li>
                                        Протекающие бессимптомно половые инфекции хронической природы – причина
                                        внутренних воспалительных процессов, результатом которых могут быть
                                        спайки
                                        маточных труб или эндометрит.
                                    </li>
                                    <li>
                                        Регулярный контакт с некоторыми химическими веществами (свинец, мышьяк, многие
                                        виды лаков и красок).
                                    </li>
                                    <li>
                                        Неблагоприятные факторы окружающей среды. Воздействие электромагнитных
                                        излучений, использование бытовой химии, низкое содержание кислорода в воздухе,
                                        воздействие УФ-излучения, – всё это провоцирует окислительный стресс и ведет к
                                        повышенной дефрагментации ДНК (дефекты в генетическом материале) сперматозоидов.
                                        При окислительном стрессе повреждаются жгутики и мембраны сперматозоидов,
                                        снижается их оплодотворяющая способность.
                                    </li>
                                    <li>
                                        Неправильный рацион. Недостаточное содержание в рационе белков (аминокислот), а
                                        также витаминов и минералов, которые участвуют в сперматогенезе, может приводить
                                        к снижению качества сперматозоидов и, как следствие, к бесплодию.
                                    </li>
                                    <li>
                                        Вредные привычки. Никотин, который поступает в организм при курении, может
                                        нарушать строение и форму сперматозоидов, а также снижать их подвижность.
                                        Хронический алкоголизм также приводит к снижению выработки сперматозоидов, а
                                        вдобавок негативно влияет на потенцию.
                                    </li>
                                    <li>
                                        Образ жизни. Бесплодие может возникнуть у тех мужчин, которые ведут
                                        малоподвижный образ жизни и имеют сидячую работу, а также у тех, кто часто
                                        принимает горячие ванны или любит ходить в баню. Это связано с тем, что из-за
                                        повышенной температуры снижается выработка сперматозоидов.
                                    </li>
                                    <li>
                                        Психологический стресс. В некоторых случаях сбои с сексуальным влечением,
                                        эрекцией и эякуляторной функцией могут быть вызваны стрессом, причем часто это
                                        происходит именно во время обследования по поводу бесплодия. Хорошие новости в
                                        том, что эти проблемы временны и решаются поддержкой близких, открытыми
                                        обсуждениями сложностей с врачом и женой. Иногда бывает полезна помощь
                                        психолога.
                                    </li>
                                </ul>
                                <p>
