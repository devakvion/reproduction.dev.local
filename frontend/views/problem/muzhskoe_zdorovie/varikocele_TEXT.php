                            <h3 class="title-article after-line">Диагностика</h3>

                            <p>
                                <b>При значительном</b> увеличении вен для постановки диагноза достаточно осмотра.
                                Если
                                внешних проявлений нет, но присутствуют болевые ощущения или снижена
                                репродуктивность – проводят УЗИ или допплерографию мошонки. Обследование обязательно
                                нужно делать в двух положениях – лежа и стоя, только так можно однозначно выявить
                                варикоцеле.
                                При варикоцеле обязательно делают спермограмму – не менее двух исследований с
                                интервалом в 4–12 недель. Перед каждой сдачей необходимо половое воздержание от 2 до
                                7 дней.
                            </p>

                            <div class="dontsplit not-col">
                                <h3 class="title-article after-line">Лечение варикоцеле</h3>

                                <p>
                                    Единственный эффективный метод лечения варикоцеле – хирургический. Само по себе
                                    заболевание не опасное, и если оно не доставляет дискомфорта, можно обойтись без
                                    операции. <br>
                                    Оперативное лечение требуется в следующих случаях:
                                </p>
                                <ul class="list-article">
                                    <li>наличие болей в области яичка;
                                    </li>
                                    <li>бесплодие;
                                    </li>
                                    <li>эстетический дефект в области мошонки;
                                    </li>
                                    <li>прекращение роста пораженного яичка при половом созревании.
                                    </li>
                                </ul>
                                <p>
                                    Многие специалисты считают, что у детей и подростков для профилактики потенциального
                                    бесплодия операцию при варикоцеле следует делать в любом случае.
                                </p>
                            </div>

                            <h3 class="title-article after-line">Лекарственная терапия</h3>

                            <p>
                                Лекарственная терапия после операции варикоцеле прежде всего направлена на
                                восстановление процессов сперматогенеза. Для восстановления фертильности после
                                операции рекомендуется прием витаминов, минералов и микроэлементов, участвующих в
                                процессах формирования и созревания сперматозоидов:
                            </p>
                            <ul class="list-article">
                                <li>
                                    L-карнитин;
                                </li>
                                <li>
                                    витамин Е;
                                </li>
                                <li>
                                    цинк;

                                </li>
                                <li>
                                    селен;
                                </li>
                                <li>
                                    фолиевая кислота.
                                </li>
                            </ul>
                            <p>
                                Перечисленные вещества улучшают количество, подвижность и активность сперматозоидов.
                                Все они входят в состав препарата <a href="/nashi_produkty/pregnoton">«СПЕРОТОН»</a>.
                                Исследования показали эффективность
