                            <h3 class="title-article after-line">Симптомы</h3>

                            <p>
                                <b>У варикоцеле</b> нет ярко выраженных симптомов. Заболевание может протекать без
                                дискомфорта, и часто его обнаруживают случайно – при проведении медицинского осмотра
                                или после результатов спермограммы. Поводом для обращения к врачу могут служить
                                следующие проявления:

                            </p>
                            <ul class="list-article">
                                <li>
                                    Увеличение и опущение левой половины мошонки. Особенно если это происходит в
                                    положении лежа и исчезает стоя.
                                </li>
                                <li>
                                    Тянущие ощущения в области яичка и паха, которые усиливаются при ходьбе,
                                    физической нагрузке и половом возбуждении.
                                </li>
                                <li>
                                    Отвислая мошонка мешает ходьбе.
                                </li>
                                <li>
                                    Постоянная боль (в запущенных случаях).
                                </li>
                            </ul>
                            <h3 class="title-article after-line">Причины и последствия</h3>

                            <p>
                                <b>Самая частая причина варикоцеле</b> – генетическая предрасположенность,
                                выраженная
                                врожденной слабостью сосудистой стенки. Практически всегда родственники больного
                                страдали варикозным расширением вен, плоскостопием, пороком клапанов сердца, фимозом
                                и другими нарушениями в соединительных тканях. <br>
                                Также проблемы с сосудами могут быть следствием анатомических изменений, когда вена
                                яичка перегибается, пережимается другими сосудами или проходит через складки тканей.
                                Это приводит к сужению вены и повышению кровяного давления. От повышенного давления
                                вены деформируются. <br>
                                Вызванное этими причинами варикозное расширение вен нарушает работу семенников.

                            </p>

                            <div class="dontsplit not-col">
                                <h3 class="title-article after-line">Перегрев мошонки</h3>

                                <p>
                                    <b>Венозная сеть</b> вокруг яичка разрастается, и оно оказывается как бы
                                    погруженным в
                                    губку из венозных сосудов. Мошонка не может охладиться до 35 °С – оптимальной
                                    температуры для сперматогенеза. В результате в яичке ухудшается выработка
                                    сперматозоидов и снижается их активность.
                                </p>
                            </div>

                            <div class="not-col">
                                <h3 class="title-article after-line">Обратный заброс венозной крови</h3>

                                <p>
                                    <b>Ослабление</b> венозных клапанов приводит к так называемому обратному забросу
                                    – к
                                    яичку поступает венозная кровь, прошедшая через надпочечники. Надпочечники, как
                                    и семенники, вырабатывают тестостерон, поэтому поступающая кровь насыщена
                                    гормонами. Рецепторы яичка определяют колоссально высокий уровень тестостерона и
                                    уменьшают выработку гормонов и сперматозоидов.
                                </p>
                            </div>

                            <h3 class="title-article after-line">Гипоксия – кислородное голодание</h3>

                            <p>
                                <b>В тканях яичка</b> падает содержание кислорода и увеличивается концентрация
                                закиси
                                азота. Яичко теряет упругость и уменьшается в размерах, повреждается зародышевый
                                эпителий. Недостаток кислорода в тканях снижает подвижность сперматозоидов.
                            </p>

                            <h3 class="title-article after-line">Накопление свободных радикалов</h3>

                            <p>
                                <b>Варикоцеле</b> сопровождается избыточным образованием свободных радикалов –
                                активных
                                форм кислорода, которые разрушают здоровые клетки. Повреждаются мембраны
                                сперматозоидов, снижается их подвижность и способность к оплодотворению. Более того,
                                свободные радикалы повреждают ДНК хромосом (генетический материал).
                                В результате всех нарушений репродуктивные способности мужчины падают вплоть до
                                полного бесплодия, но потенция при этом может быть высокой.
                            </p>
