                            <h3 class="title-article after-line">Витамины и антиоксиданты</h3>

                            <p>
                                Мастопатия напрямую связана с оксидативным (окислительным) стрессом. Под
                                воздействием негативных факторов в организме образуется избыточное количество
                                свободных радикалов (окислителей) – чрезвычайно активных молекул, разрушающих
                                здоровые клетки. Это приводит к нарушению обмена веществ, необратимым повреждениям
                                ДНК и сбоям в работе многих органов. Ткани молочных желез особо чувствительны к
                                такого рода воздействию и одними из первых страдают от атак свободных радикалов. В
                                нормальном состоянии организм справляется со свободными радикалами сам, во время
                                оксидативного стресса ему нужна помощь.
                                Для нейтрализации свободных радикалов в большинство схем лечения мастопатии включают
                                антиоксиданты. Самые эффективные – коэнзим Q <sub>10</sub>, ликопин, рутин, витамины
                                А, С и Е.
                                Помимо прямого действия, антиоксиданты усиливают терапевтическую активность других
                                лекарств, устраняют или ослабляют их побочные эффекты, стабилизируют нервную систему
                                и укрепляют иммунитет. Принимают антиоксиданты и во время лечения, и для
                                профилактики. </p>
                            <ul class="list-article">
                                <li>Витамин E уменьшает разрастание тканей, усиливает положительное действие гормона
                                    прогестерона и нормализует работу половых желез.
                                </li>
                                <li> Рутин и витамин C улучшают микроциркуляцию и уменьшают локальный отек молочной
                                    железы.
                                </li>
                                <li>
                                    Витамин A снижает негативное влияние женских половых гормонов на молочную железу
                                    и
                                    уменьшает разрастание эпителия.
                                </li>
                            </ul>
                            <p>
                                Курсы приема витаминов при мастопатии – длительные. Вместо витамина А в чистом виде
                                безопаснее принимать бета-каротин, который в организме превращается в витамин А.
                                Дело в том, что при длительном приеме избыток витамина А накапливается в организме и
                                нарушает работу печени и других органов. Бета-каротин такого влияния не оказывает,
                                так как из него образуется ровно столько витамина А, сколько требуется организму.
                                Кроме того, бета-каротин способствует снижению болевого синдрома при мастопатии.
                            </p>

                            <h3 class="title-article after-line">Диета</h3>

                            <p>
                                Существует тесная взаимосвязь между развитием фиброзно-кистозной мастопатии и
                                употреблением кофе, чая, шоколада, какао и колы. В этих продуктах содержатся
                                вещества метилксантины, которые провоцируют развитие мастопатии. Полный или хотя бы
                                частичный отказ от этих продуктов может существенно уменьшить боли и чувство
                                напряжения в молочных железах.
                            </p>

                            <h3 class="title-article after-line">Профилактика</h3>

                            <p>
                                Женщинам и девушкам, входящим в группу риска, стоит обратить особое внимание на свой
                                образ жизни. Для профилактики мастопатии рекомендуется свести к минимуму
                                употребление кофе, чая, шоколада и колы или полностью отказаться от них.
                                Нежелательно загорать и долго находиться на солнце. Противопоказано курение. <br>
                                Отдельно стоит остановиться на негативном влиянии окружающей среды. Недостаток
                                кислорода, загрязненный воздух, постоянная близость к источникам электромагнитного
                                излучения – все это повышает риски появления и развития мастопатии. Однако далеко не
                                всегда реально сменить место жительства на более экологичное. <br> Поэтому женщинам,
                                живущим в больших городах, особенно важно регулярно наблюдаться у врача и стараться
                                уменьшить воздействие вредных факторов на организм. Обратите внимание, что одна из
                                сфер применения комплекса <a href="/nashi_produkty/sinergin">«Синергин»</a> – как
                                раз нивелирование негативного влияния
                                окружающей среды на репродуктивную систему, в том числе и на молочные железы.

                            </p>
