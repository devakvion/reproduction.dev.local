                            <h3 class="title-article after-line">Как проявляется мастопатия</h3>

                            <p>
                                Основные признаки – болезненность груди, повышение чувствительности, нагрубание,
                                отечность. Болевые ощущения чаще всего появляются во второй половине цикла и
                                усиливаются за несколько дней до менструации. Характер и интенсивность боли могут
                                быть разными. Некоторые женщины вообще не испытывают никаких болевых ощущений и
                                обращаются к врачу, лишь обнаружив уплотнения.
                            </p>

                            <h3 class="title-article after-line">Диагностика</h3>

                            <p>
                                <b>Чем раньше выявить мастопатию – тем проще ее победить. Диагностику можно условно
                                    разделить на три уровня.</b>
                            </p>
                            <ul class="list-article">
                                <li>Самообследование – ощупывание груди, оценка собственных ощущений.
                                </li>
                                <li>Осмотр профильным специалистом. Лучше всего проходить регулярный осмотр у
                                    маммолога. Если такого специалиста в поликлинике нет – обратиться к гинекологу,
                                    онкологу или хирургу.
                                </li>
                                <li>
                                    Уточняющая диагностика – анализы, исследования, тесты.
                                </li>
                            </ul>
                            <h3 class="title-article after-line">Лечение</h3>

                            <p>
                                Единой схемы лечения мастопатии не существует. Каждый случай требует индивидуального
                                подхода – патология может проявляться по-разному и иметь разные формы. Задача врача
                                – избавить пациента от боли и неприятных ощущений, остановить прогрессирование
                                заболевания и, если это возможно, выявить и устранить причины.
                                Современный уровень развития медицины позволяет в большинстве случаев обойтись без
                                хирургического вмешательства. Консервативное лечение подбирается врачом и часто
                                обходится даже без гормональных препаратов.
                            </p>
