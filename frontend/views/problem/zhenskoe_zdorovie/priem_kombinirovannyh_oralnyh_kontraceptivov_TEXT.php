                                        <h3 class="title-article after-line">Что происходит при приеме оральных
                                            контрацептивов?</h3>

                                        <p>
                                            <b>Давайте разберемся</b>, как работают оральные контрацептивы. Они
                                            «поставляют»
                                            в
                                            организм
                                            женщины
                                            дополнительные порции гормонов, подавляющих овуляцию. Овуляция —
                                            единственный
                                            процесс в
                                            организме, на который должны влиять контрацептивы, никак не нарушая работу
                                            других
                                            органов и
                                            систем. Однако дополнительные гормоны — всегда дисбаланс, ведь гормоны
                                            регулируют не
                                            один-единственный процесс в организме. Например, эстрогены участвуют в
                                            терморегуляции,
                                            от
                                            них зависит прочность костей и пигментация. Поэтому «приказать» таблеткам
                                            влиять
                                            только
                                            на
                                            овуляцию, не затрагивая все остальные сферы действия гормонов, входящих в
                                            состав
                                            комбинированных контрацептивов, – не получится.
                                        </p>
                                    </div>

