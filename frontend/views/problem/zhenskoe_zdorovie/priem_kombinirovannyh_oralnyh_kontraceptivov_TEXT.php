                                <h3 class="title-article after-line">Чего опасаться? </h3>

                                <p>
                                    <b>Самые распространенные побочные эффекты приема комбинированных оральных
                                        контрацептивов: </b>
                                </p>
                                <ul class="list-article">
                                    <li>«Вымывание» витаминов и минералов. Больше всего контрацептивы бьют по
                                        витаминам
                                        группы В
                                        (В2, В6, В12, В9), витамину С, магнию, цинку, йоду и селену.
                                    </li>
                                    <li>Небольшое повышение артериального давления у 3–5 % женщин (что немало,
                                        если
                                        посчитать
                                        общее количество женщин, принимающих средства).
                                    </li>
                                    <li>
                                        Ухудшение течения гипертонической болезни. Это может случиться из-за
                                        дефицита
                                        магния,
                                        который на фоне приема контрацептивов неблаготворно сказывается на
                                        работе
                                        сердечно-сосудистой системы.

                                    </li>
                                    <li>
                                        Напряжение или нагрубание груди (мастодиния, масталгия) связаны с
                                        дефицитом
                                        витамина В
                                        <sub>6</sub>.
                                    </li>
                                    <li>«Кровомазанье». Это одно из видимых проявлений нарушенной регуляции
                                        гормональных
                                        процессов. Витамины А, С, Е играют важную роль в работе эндокринной
                                        системы.
                                        Витамины С
                                        и Е определяют чувствительность тканей к половым гормонам, поэтому при
                                        их
                                        нехватке общее
                                        состояние репродуктивной системы может ухудшиться. Если при этом еще
                                        возникнет
                                        недостаток бета-каротина, слизистые станут легче травмироваться и хуже
                                        восстанавливаться. Кроме того, если организму постоянно не хватает этих
                                        витаминов, то
                                        после отмены КОК будет сложно восстановить регулярный цикл.
                                    </li>
                                    <li>Слабый синтез ферментов-антиоксидантов усиливает оксидативный стресс
                                        (избыток
                                        свободных
                                        радикалов) – одну из причин старения. Свободные радикалы воздействуют на
                                        все
                                        органы и
                                        ткани. Мы не видим их разрушающего действия на отдельные клетки, но
                                        замечаем
                                        проявления
                                        оксидативного стресса, когда он повреждает уже целые органы и системы.
                                        Кожа
                                        теряет
                                        эластичность, замедляется обновление клеток, сосуды становятся более
                                        хрупкими,
                                        начинает
                                        сбоить эндокринная система, репродуктивная система работает нестабильно.
                                        Не
                                        говоря уже о
                                        том, что возникают проблемы с обменом веществ, которые могут привести к
                                        таким
                                        серьезным
                                        болезням как мастопатия и диабет.
                                    </li>
                                    <li>Повышенная угроза тромбообразования – нехватка витамина Е провоцирует
                                        слипание
                                        тромбоцитов (клеток крови, отвечающих за свертываемость). Могут
                                        появиться
                                        некрасивые
                                        сосудистые сеточки – первый симптом венозной недостаточности.
                                    </li>
                                    <li>Проблемы с кожей: круги под глазами, проявление темных пятен на солнце,
                                        медленное
                                        заживание царапин и т.д.
                                    </li>
                                    <li>Ухудшение работы печени и желчного пузыря. Гормоны-стероиды, входящие в
                                        состав
                                        контрацептивов, повышают выработку холестерина. Кроме того, стероиды
                                        губительно
                                        действуют на клетки печени, ухудшая их работу. Могут возникнуть
                                        неприятные
                                        ощущения,
                                        ухудшение работы ЖКТ.
                                    </li>
                                </ul>
