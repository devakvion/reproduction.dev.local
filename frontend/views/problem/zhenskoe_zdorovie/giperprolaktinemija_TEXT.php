                                <h3 class="title-article after-line">Что влияет на уровень пролактина?</h3>

                                <p>
                                    Выделение пролактина гипофизом можно описать как механизм, не имеющий кнопки
                                    включения.
                                    Присутствующий в организме дофамин в норме постоянно угнетает секрецию пролактина,
                                    образно
                                    говоря, держит нажатой кнопку «выкл». Повышенное содержание в крови женского гормона
                                    эстрогена стимулирует производство пролактина, а сам пролактин снижает секрецию
                                    эстрогена и
                                    тормозит возбуждающее действие дофамина. Эти три элемента находятся в уравновешенной
                                    взаимосвязи, где каждый воздействует на остальных. Например, дефицит дофамина или
                                    избыток
                                    эстрогена автоматически влекут за собой усиление секреции пролактина.
                                    Содержание пролактина в крови может меняться в течение суток и зависит от многих
                                    факторов.
                                    Естественным является его повышение во второй половине менструального цикла, в
                                    периоды
                                    беременности и кормления грудью. Самая высокая концентрация на протяжении суток
                                    отмечается
                                    во время сна и ранним утром. Причиной кратковременного или продолжительного
                                    повышения
                                    уровня
                                    пролактина могут быть стресс, боль, физическая и сексуальная активность, даже
                                    обычное
                                    волнение. Такие колебания физиологичны и возвращаются к норме естественным путем.
                                </p>
