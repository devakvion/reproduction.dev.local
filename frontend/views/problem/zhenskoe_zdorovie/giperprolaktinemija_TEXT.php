                            <h3 class="title-article after-line">Как подготовиться к анализу на
                                гиперпролактинемию?</h3>

                            <p>
                                <b>Кровь на анализ для определения уровня пролактина берут из вены. Выполнение
                                    следующих условий поможет избежать ложных результатов:
                                </b>
                            </p>
                            <ul class="list-article">
                                <li>сдавать кровь натощак, не ранее чем через час после пробуждения;
                                </li>
                                <li>женщинам нужно сдавать этот анализ в первую фазу цикла (5-7 день);
                                </li>
                                <li>накануне следует воздержаться от посещения сауны или бани, употребления алкоголя
                                    и сексуальных контактов;
                                </li>
                                <li>
                                    тревога и даже небольшое волнение могут повлиять на результат; избегайте
                                    стрессов перед сдачей анализа.
                                </li>
                            </ul>
                            <p>
                                Анализ рекомендуется сдавать несколько раз, обычно трижды в разные дни. Для
                                подтверждения диагноза нужны два результата с превышенным содержанием пролактина; в
                                большинстве лабораторий верхней границей нормы считают 500 Мме/л, или 25 нг/мл.
                                Уровень пролактина в плазме крови в некоторой степени может помочь врачу определить
                                причины гиперпролактинемии.
                            </p>

                            <h3 class="title-article after-line">Методы лечения гиперпролактинемии</h3>

                            <p>
                                <b>
                                    В зависимости от результата анализов и комплекса сопутствующих симптомов
                                    могут быть назначены:
                                </b>
                            </p>
                            <ul class="list-article">

                                <li>
                                    дополнительные анализы для определения уровня других гормонов, в числе которых
                                    гормоны щитовидной железы, эстрадиол, лютеинизирующий гормон (ЛГ) и
                                    фолликулостимулирующий гормон (ФСГ);
                                </li>
                                <li>
                                    консультация офтальмолога и магнитно-резонансная томография головного мозга для
                                    исключения опухоли гипофиза или гипоталамуса.
                                </li>
                            </ul>
                            <p>
                                Способы лечения гиперпролактинемии выбираются в зависимости от ее причин.
                                В случае обнаружения пролактиномы на первый план выходит необходимость уменьшения ее
                                размера и контроль роста. Для этого используются главным образом консервативная
                                терапия. Подобные опухоли обычно доброкачественны и локальны, они редко нуждаются в
                                хирургическом вмешательстве.
                                Состояния, вызванные идиопатической (функциональной) гиперпролактинемией,
                                корректируют в зависимости от симптомов, индивидуальным подбором лекарственных
                                средств. Наиболее эффективно себя проявляют препараты из группы агонистов дофамина,
                                стимулирующие дофаминовые рецепторы. К сожалению, агонисты дофамина могут вызывать
                                ряд побочных эффектов, а кроме того, не до конца изучена их безопасность в области
                                отдаленных последствий для потомства.
                                Поэтому, если женщина планирует беременность, а уровень пролактина незначительно
                                превышает норму (не более 600–1000 Мме/л), для его снижения применяются более мягкие
                                средства. Например, экстракт растения витекс священный, плоды и листья которого
                                содержат фитоэстрогены и дофаминергические вещества растительного происхождения,
                                стимулирующие выработку дофамина. Экстракт витекса входит в состав комбинированного
                                препарата <a href="/nashi_produkty/pregnoton">«Прегнотон»</a>, чья эффективность при
                                данной проблеме подтверждена
                                исследованиями.
                            </p>
