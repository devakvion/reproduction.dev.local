                            <h3 class="title-article after-line">Почему возникает гиперпролактинемия?</h3>

                            <p>
                                <b>Повышенное содержание пролактина в крови называется гиперпролактинемией. Обычно
                                    выделяют три группы причин ее возникновения: фармакологические,
                                    патологические и функциональные.
                                </b>
                            </p>
                            <ul class="list-article">
                                <li>К фармакологическим причинам повышения уровня пролактина относятся
                                    прием эстрогенсодержащих оральных контрацептивов и некоторых лекарств, в том
                                    числе нейролептиков, антидепрессантов и противосудорожных препаратов, курение и
                                    употребление наркотиков.
                                </li>
                                <li>Патологические причины. Повышенный уровень пролактина является
                                    следствием
                                    другого заболевания: опухоли гипофиза, поликистоза яичников, почечной и
                                    печеночной недостаточности, цирроза печени, недостаточной функции щитовидной
                                    железы, перенесенного радиационного облучения или операции на грудной клетке. В
                                    таких случаях гиперпролактинемия – не более чем симптом и требует лечения
                                    основной болезни.
                                </li>
                                <li>
                                    Функциональная, или идиопатическая гиперпролактинемия возникает из-за
                                    гиперсекреции, повышенной функции клеток, продуцирующих пролактин. Причины
                                    гиперсекреции пока не обнаружены, но существуют методы медикаментозной коррекции
                                    этого состояния.

                                </li>
                            </ul>
                            <div class="dontsplit not-col">
                                <h3 class="title-article after-line not-col-up">Какие симптомы позволяют предположить
                                    наличие
                                    гиперпролактинемии?</h3>

                                <p>
                                    <b>Наиболее широкий спектр симптомов относится к репродуктивной сфере.

                                    </b>
                                </p>
                                <ul class="list-article">

                                    <li>
                                        Чаще всего это нарушения менструального цикла, ненаступление беременности,
                                        утрата сексуального желания – все они следствие нарушения баланса пролактина и
                                        эстрогена.
                                    </li>
                                    <li>В случаях очень высокого уровня пролактина могут развиться галакторея (выделение
                                        молока из сосков) и гипермастия (разрастание тканей груди).
                                    </li>
                                    <li>Гиперандрогения (нарушение баланса «женских» и «мужских» гормонов) и гирсутизм
                                        (избыточный рост волос на теле).
                                    </li>
                                    <li>Повышенная угроза тромбообразования – нехватка витамина Е провоцирует
                                        слипание
                                        тромбоцитов (клеток крови, отвечающих за свертываемость). Могут появиться
                                        некрасивые
                                        сосудистые сеточки – первый симптом венозной недостаточности.
                                    </li>
                                    <li>Психоэмоциональные расстройства (быстрая утомляемость, эмоциональная
                                        неустойчивость, снижение жизненного тонуса, нарушения сна) обычно связаны с
                                        дефицитом или подавленной функцией дофамина.

                                    </li>
                                </ul>
                                <p>
                                    Если причиной гиперпролактинемии является опухоль гипофиза, к перечисленным выше
                                    симптомам могут добавиться головная боль, снижение остроты или сужение поля зрения.
                                    Наличие описанных симптомов в любом их сочетании – повод для обращения за
                                    консультацией
                                    к специалисту. Гинеколог и эндокринолог назначат анализ на гормоны и по его
                                    результатам
                                    сделают заключение о диагнозе.
                                </p>
