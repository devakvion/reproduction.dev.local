                            <h3 class="title-article after-line">Виды нарушений</h3>

                            <p>
                                Специалисты выделяют несколько основных видов «ненормальных» менструальных циклов.
                                Приведем ниже расшифровки этих терминов.
                            </p>
                            <ul class="list-article">
                                <li>
                                    Аменорея – полное отсутствие менструаций три месяца и больше.
                                </li>
                                <li>
                                    Гиперменорея – менструация слишком обильная, но наступает по графику и длится
                                    примерно одинаковое количество дней.
                                </li>
                                <li>Гипоменорея – очень скудные выделения, но происходят регулярно.</li>
                                <li>
                                    Меноррагия – очень обильная менструация, длящаяся дольше недели, наступает
                                    регулярно.
                                </li>
                                <li>Полименорея – менструация длится более недели, но без обильных выделений.</li>
                                <li>
                                    Олигоменорея – длится меньше трех дней, количество выделений в норме,
                                    регулярность не нарушена.
                                </li>
                                <li>
                                    Опсоменорея – редкие менструации (цикл от 36 дней до трех месяцев), длительность
                                    и количество выделений в норме.
                                </li>
                                <li>
                                    Пройоменорея – частые менструации (цикл менее 21 дня), но длительность и
                                    количество выделений в норме.
                                </li>
                                <li>
                                    Метроррагия – маточное кровотечение вне дней предполагаемой менструации, может
                                    быть различной длительности и интенсивности.
                                </li>
                            </ul>
