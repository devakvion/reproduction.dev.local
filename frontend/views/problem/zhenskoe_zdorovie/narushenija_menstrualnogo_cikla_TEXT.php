                        <div class="columh-block">
                            <h3 class="title-article after-line">Диагностика: осмотр, анализы, УЗИ</h3>

                            <p>
                                Поскольку причины нарушения менструального цикла могут лежать в самых разных сферах,
                                врач начинает диагностический поиск от простого к сложному.
                            </p>

                            <p>
                                Первый этап – сбор анамнеза. Врач выясняет, какие препараты принимает женщина, была
                                ли недавно беременность, много ли в ее жизни стрессовых ситуаций, пытается
                                определить, в каком психическом состоянии она находится.
                            </p>

                            <p>
                                Далее следует внешний осмотр. Цвет кожи, слизистой, склер, нет ли признаков
                                истощения или лишнего веса и т.п. Также врач обязательно проведет гинекологический
                                осмотр и возьмет мазки со слизистых для определения возможных инфекций.
                            </p>

                            <p>
                                Если необходимо, женщина получает направление на УЗИ малого таза. Если оно не
                                покажет патологий в состоянии матки и яичников, делается УЗИ всех органов и систем,
                                прицельное УЗИ щитовидной железы и печени.
                            </p>

                            <p>
                                Клинические и биохимические анализы крови позволяют составить коагулограмму (набор
                                показателей крови, указывающий на процесс свертываемости), выяснить, в каком
                                состоянии находятся внутренние органы, кроветворная ткань.
                            </p>

                            <p>
                                Определение уровня гормонов в крови. При нарушении цикла определяют содержание
                                эстрадиола, прогестерона, лютеинизирующего гормона (ЛГ), фолликулостимулирующего
                                гормона (ФСГ), пролактина (ПРЛ). <br> Анализы на разные гормоны сдаются в разные дни
                                цикла, поэтому будьте готовы, что придется посетить лабораторию несколько раз.
                            </p>

                            <p>
                                Сочетание анамнеза, результатов исследований и анализов позволит найти причины
                                нерегулярных менструаций, болей и патологических кровотечений и устранить их.
                            </p>

                            <h3 class="title-article after-line">Переходим к лечению</h3>

                            <p>
                                Основной подход к нарушениям менструального цикла – излечение основного заболевания.
                                Например, при хроническом воспалительном процессе назначают антибиотики, при
                                пониженной или повышенной массе тела следует поработать с диетологом и, возможно, с
                                психологом. Только после устранения основного очага заболевания и наиболее
                                острых
                                клинических проявлений можно заниматься непосредственно восстановлением нарушенного
                                цикла.
                            </p>

                            <p>
                                Как правило, в современной медицине коррекцию и восстановление цикла начинают с
                                рекомендаций, направленных на общее укрепление организма и восстановление его
                                нормальной работы: соблюдение диеты, прием витаминных комплексов, возможно,
                                препаратов железа. Нередко потребуется уменьшить физические нагрузки или даже
                                сменить работу на менее стрессовую. <br>
                                И только если эти методы не работают, специалисты используют гормональные препараты
                                (оральные контрацептивы, прогестагены).
                            </p>

                            <h3 class="title-article after-line">Роль витаминов и растительных экстрактов</h3>

                            <p>
                                Многие женщины, получив рекомендации принимать витамины, считают, что врач «ничего не
                                назначил» и обвиняют его в некомпетентности. Но в действительности витамины и минералы
                                имеют большое значение в стабилизации гормонального фона. Ведь роль витаминов в
                                организме – регуляция многих обменных процессов, синтеза гормонов и чувствительность к
                                ним многих органов-мишеней. Исследования показали, что некоторые витамины
                                непосредственно влияют на функцию репродуктивной системы.
                            </p>

                            <p>
                                Витамины В1, В2, В6, Е, фолиевая кислота обладают гонадотропным действием (стимулируют
                                функцию половых желез) и участвуют в обмене эстрогенов. Витамины С, Е, В6 улучшают
                                координационные связи в репродуктивной системе.
                            </p>

                            <p>
                                Часто витамины используют как негормональную терапию для восстановления менструального
                                цикла у подростков и женщин, в том числе перенесших операции на яичниках.
                            </p>

                            <p>
                                Также для коррекции гормонального фона специалист может назначить экстракт витекса
                                священного. Это растение эффективно снижает уровень пролактина при его повышенном
                                синтезе, облегчает болевой синдром при ПМС. Если причиной нарушенного цикла была
                                гиперпролактинемия (повышенное содержание пролактина), то нередко после 1-3 месяцев
                                приема препаратов витекса восстанавливается нормальный цикл.

                            </p>

                            <p>
                                Экстракт витекса, а также витамины и минералы, необходимые для восстановления цикла,
                                входят в состав негормонального препарата <a href="/nashi_produkty/pregnoton">«ПРЕГНОТОН»</a>.
                            </p>
