                            <div class="not-col dontsplit">
                                <h3 class="title-article after-line">Не болезнь, а сигнал</h3>

                                <p>
                                    <b>Нарушения менструального цикла</b> – не заболевание, а показатель, что организм
                                    не в
                                    порядке. Они могут быть связаны не только с нарушениями в половой сфере, но и с
                                    общими,
                                    и с эндокринными заболеваниями. Поэтому обязательно нужно обратить внимание на эти
                                    сигналы и обратиться к гинекологу, чтобы найти их причину.
                                </p>
                            </div>
                            <div class="not-col dontsplit">
                                <h3 class="title-article after-line">Что провоцирует нарушения</h3>

                                <p>
                                    Давайте рассмотрим основные причины нарушений менструального цикла.
                                </p>
                                <ul class="list-article">
                                    <li>
                                        Гормональный дисбаланс. Он может быть вызван не только половыми гормонами, но и
                                        нарушением работы щитовидной железы, гипофиза, надпочечников. До 70 % случаев
                                        менструальных нарушений гормонального характера случаются из-за
                                        гиперпролактинемии – состояния, при котором в крови повышено содержание гормона
                                        пролактина. Откуда он берется? Выбрасывается в организм в каждой стрессовой
                                        ситуации, даже самой бытовой и повседневной. Чем больше стресса – тем выше
                                        вероятность гормонального сбоя и нарушения цикла.
                                    </li>
                                    <li>
                                        Хронический стресс или заболевания нервно-психической сферы. Отчасти стресс
                                        связан с нарушением гормонального баланса.
                                    </li>
                                    <li>
                                        Инфекционно-воспалительные заболевания органов малого таза.
                                    </li>
                                    <li>
                                        Истощение или лишний вес. И то и другое состояние одинаково плохо влияет на
                                        регулярность цикла.
                                    </li>
                                    <li>
                                        Нарушения питания: при недостатке в рационе некоторых аминокислот, витаминов и
                                        минералов нарушаются процессы овуляции.
                                    </li>
                                    <li>
                                        Лекарства – транквилизаторы, антидепрессанты, антикоагулянты (препятствуют
                                        сворачиванию крови), препараты заместительной гормональной терапии. Всегда
                                        рассказывайте гинекологу о препаратах, прописанных другими специалистами.
                                    </li>
                                </ul>
