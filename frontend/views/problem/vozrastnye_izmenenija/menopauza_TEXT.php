                                <h3 class="title-article after-line">Что такое менопауза? </h3>

                                <p>
                                    <b>Менопауза наступает</b>, когда яичники истощаются и в них перестают созревать
                                    яйцеклетки. Это значит, что женщина больше не сможет забеременеть, выносить и
                                    родить
                                    ребенка. В это время в крови женщины повышается содержание
                                    фолликулостимулирующего
                                    гормона (ФСГ), который контролирует деятельность яичников. Естественно, что
                                    изменяется уровень и остальных гормонов. А так как гормоны – это регуляторы всех
                                    процессов в нашем организме, то начинает меняться весь организм.
                                    Изменения в женском организме происходят не сразу, а постепенно, этот процесс
                                    можно
                                    разделить на несколько стадий:
                                </p>
                                <ul class="list-article">
                                    <li>Пременопауза — с начала нерегулярных менструаций до полного их окончания.
                                    </li>
                                    <li>Менопауза — период в год (12 месяцев) после окончания последней менструации.
                                        Обычно менопауза наступает в возрасте 50 лет, но бывают и ранние случаи
                                        менопаузы (40 лет), и даже преждевременные (37-39 лет).
                                    </li>
                                    <li>
                                        Постменопауза — сразу после менопаузы и до возраста в 65-69 лет.
                                        Постменопауза
                                        также делится на 2 периода: ранняя (до 5 лет) и поздняя (от 5 до 10 лет).
                                    </li>
                                </ul>
                                <p>
                                    Сроки наступления менопаузы передаются по наследству. Примерно в то же время,
                                    когда
                                    менопауза началась у матери, следует обратиться ко врачу и дочери. Первыми
