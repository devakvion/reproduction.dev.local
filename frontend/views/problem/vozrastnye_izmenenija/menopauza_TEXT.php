                            <h3 class="title-article after-line">Как узнать про менопаузу?</h3>

                            <p>
                                Пока функции яичников постепенно гаснут, как свет в театре, в организме женщины
                                снижается уровень определённого гормона — эстрогена. Эта перестройка
                                сопровождается
                                изменениями, которые женщина уже может почувствовать: эмоциональная
                                нестабильность,
                                беспокойство, депрессия, ухудшение памяти и повышенная утомляемость, приливы
                                жара,
                                ознобы, повышенная потливость, головные боли, гипотония или гипертензия,
                                учащенное
                                сердцебиение. <br>
                                Многие женщины привыкли держать себя в «ежовых рукавицах» и не обращают внимания
                                на
                                эмоциональное состояние. Но именно раздражительность, сонливость, слабость,
                                невнимательность, забывчивость являются самыми первыми сигналами о
                                приближающихся
                                переменах. Второй явный признак – снижение либидо. <br>
                                К сожалению, дефицит эстрогена нарастает и начинает плохо влиять и на другие
                                сферы
                                жизни. Возрастает риск сердечно-сосудистых заболеваний, изменяется обмен глюкозы
                                и
                                инсулина. Всё это сопровождаться снижением кровообращения во влагалище, так что
                                женщину могут начать беспокоить не только зуд и жжение в интимных местах, боль
                                при
                                занятиях сексом, но и вагинальные или даже периодические кровянистые выделения.
                                В
                                самых неприятных случаях дело доходит до уретрального синдрома, цисталгии и
                                недержания мочи. <br>
                                Сказывается дефицит эстрогенов и на внешности. Кожа становится сухой, ногти –
                                ломкими, появляются морщины, начинают выпадать волосы. Либидо может снизится до
                                нуля. <br>
                                Кроме того, эстрогены требуются для ассимиляции кальция костной тканью, поэтому
                                в
                                период менопаузы резко возрастает риск травм и переломов, которые сложно
                                заживают, а
                                также заболеваний опорно-двигательного аппарата: остеопороза и остеоартритов.
                            </p>

