                                </li>
                                <li>
                                    Сухость влагалища.
                                </li>
                                <li>
                                    Частые мочеиспускания, потребность в ночном мочеиспускании.
                                </li>
                                <li>
                                    Повышенный риск инфекций мочевого пузыря (встречается у 50 % женщин, с
