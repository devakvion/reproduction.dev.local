                                    <h3 class="title-article after-line">На что обращать внимание?</h3>

                                    <p>
                                        <b>Примерно в 35 лет</b> женский организм начинает постепенно перестраиваться.
                                        С 35-40 лет у женщин уменьшается выработка гормона прогестерона. Прогестерон
                                        защищает эндометрий и молочные железы от эстрогенной стимуляции. Дефицит
                                        прогестерона оказывает неблагоприятное влияние на женский организм, в частности,
                                        повышает частоту доброкачественных заболеваний молочных желез в 5 раз. Поэтому
                                        после
                                        35 лет желательно посещать специалиста-маммолога или гинеколога хотя бы раз в
                                        год.
                                        Также будет полезен прием бета-каротина, витамина Е, рутина и витамина С,
                                        которые
                                        улучшают состояние молочных желез и уменьшают вероятность развития мастопатии.
                                        <br>
                                        Еще одна «женская» проблема – повторяющиеся (рецидивирующие) циститы. Чем старше
                                        женщина, тем выше риск циститов. Даже если до тридцати лет женщина не
                                        сталкивалась с
                                        этим заболеванием, то это не гарантия, что так будет всегда. Важно помнить об
                                        этом и
                                        при первых же симптомах обратиться к врачу. <br> А затем проводить профилактику
                                        цистита.
                                        В частности, витамин С снижает вероятность повторного развития цистита в 2 раза,
                                        а
                                        проантоцианидины клюквы защищают мочевой пузырь от инфекций. Поэтому препараты с
                                        высоким содержанием проантоцианидинов клюквы и витамина С, принимаемые курсом
                                        после
                                        острого заболевания или разово после переохлаждения, посещения бассейна и т. д.
                                        помогут уменьшить вероятность повторного цистита. <br>
                                        Примерно после 45 лет уровень женских половых гормонов начинает падать уже более
