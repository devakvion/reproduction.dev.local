                                <h3 class="title-article after-line">Чтобы стать мамой после 35 лет</h3>

                                <p>
                                    <b>После 35 лет</b> начинает снижаться функция яичников. Это значит, что
                                    вероятность
                                    зачатия несколько уменьшается, но женщина может забеременеть. Однако из-за того,
                                    что
                                    состояние репродуктивной системы с годами ухудшается, а яичники постепенно
                                    истощаются, то и зачатие может наступить не сразу, выше риск прерывания
                                    беременности
                                    и ее осложнений. <br> Дело в том, что репродуктивная система наиболее уязвима к
                                    воздействию негативных факторов окружающей среды, на яичниках и матке
                                    сказываются
                                    перенесенные заболевания и прием некоторых лекарств, а также гормональных
                                    контрацептивов. Все эти факторы в сочетании с возрастным уменьшением активности
                                    работы репродуктивной системы делают зачатие и беременность уже несколько более
                                    сложной «задачей» для организма, чем пятнадцать лет назад. Но скорость, с
                                    которой
                                    наша репродуктивная система начинает «стареть», можно изменить. <br>
                                    Если вы принимаете оральные контрацептивы – включайте в свой рацион
                                    дополнительный
                                    прием антиоксидантов, чтобы уменьшить их негативное влияние на организм. Кроме
                                    того,
                                    антиоксиданты будут полезны всем женщинам, живущим в городах, где особенно много
                                    факторов, вызывающих оксидативный стресс в организме, который является одной из
                                    причин старения. <br> Также важно помнить, что прием оральных контрацептивов
                                    провоцирует
                                    дефицит важных витаминов и минералов, которые необходимы для нормальной работы
