                                <h3 class="title-article after-line">Внешность – это важно!</h3>

                                <p>
                                    <b>Внешность не относится</b> к работе репродуктивной системы, но говоря о
                                    возрастных
                                    изменениях нельзя обойти эту тему. Многих женщин, которые приближаются к
                                    сорокалетию, серьезно волнуют внешние проявления старения. Морщины, потеря
                                    упругости
                                    кожи, сосудистые сеточки на ногах, ухудшение зрения, снижение тонуса и
                                    недостаток
                                    жизненной энергии… Можно ли справиться с этими приметами времени? <br>
                                    Оказывается, и это возможно. Во многих изменениях, которые происходят в нашем
                                    организме, виноваты не прожитые нами годы, а свободные радикалы. Просто со
                                    временем
                                    их разрушительное действие «накапливается» и становится более заметным. <br>
                                    Избыток
                                    свободных радикалов приводит к разрушению коллагена и эластина – природного
                                    каркаса
                                    кожи, в результате ухудшается ее состояние. Влияние свободных радикалов на
                                    сосуды
                                    проявляется в снижении их эластичности, ухудшении проводимости, как итог – на
                                    сосудах появляются отложения, они перестают справляться с оттоком крови, на
                                    ногах
                                    проступают «сеточки». <br> Другая сторона этого же явления – ухудшение
                                    микроциркуляции в
                                    тканях глаз и возрастные ухудшения зрения. На клеточном уровне свободные
                                    радикалы
                                    тоже представляют угрозу. Они разрушают генетический материал клетки, нарушают
                                    работу клеточных органелл, влияют на выработку энергии. <br>
                                    Поэтому дополнительный прием антиоксидантов поможет устранить избыток свободных
                                    радикалов и тем самым замедлить возрастные изменения.
                                    В <a href="/nashi_produkty/sinergin">Синергине</a> содержится 6 мощных природных
                                    антиоксидантов с доказанным действием,
                                    которые эффективно работают во всех органах и тканях.
                                </p>

