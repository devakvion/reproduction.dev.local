                                    <h3 class="title-article after-line">Что происходит с организмом?</h3>

                                    <p>
                                        <b>Поскольку</b> речь идет не о разовом падении уровня тестостерона, а о его
                                        хронической
                                        недостаточности, изменения медленно сказываются на работе различных органов и
                                        систем
                                        и могут постепенно привести к снижению качества жизни. <br>
                                        <b>Основные клинические признаки и проявления андрогенного дефицита:</b>
                                    </p>
                                    <ul class="list-article">
