                                <h3 class="title-article after-line">Как возраст сказывается на фертильности?</h3>

                                <p>
                                    <b>В результате возрастных</b> изменений возникают проблемы с зачатием, а здоровье
                                    родившихся малышей может оказаться под вопросом. С 35-40 лет нарушается процесс
                                    образования зрелых мужских половых клеток (сперматогенез), увеличиваются мутации
                                    сперматозоидов (на 4% в год), чаще встречаются хромосомные аномалии. Это может
                                    привести к неблагоприятным исходам беременности или врожденным дефектам и
                                    заболеваниям у потомства. <br>
                                    Возрастает риск хронического простатита, аденомы (доброкачественной гиперплазии) и
                                    даже рака предстательной железы. Все эти заболевания сказываются на возможности
                                    иметь детей. <br>
                                    Кроме того, с возрастом в организме скапливаются свободные радикалы, которые
                                    разрушают и деформируют клетки организма. Рост числа свободных радикалов приводит к
                                    оксидативному стрессу. С годами он только увеличивается, а жители городов подвержены
                                    ему в наибольшей степени. Оксидативный стресс, во-первых, может быть одной из причин
                                    эректильной дисфункции, а во-вторых, повреждает генетический материал сперматозоидов
                                    и может лежать в основе привычных выкидышей или врожденных аномалий. <br>
                                    Поэтому если вы старше 35 лет и еще рассматриваете возможность стать отцом, то
                                    желательно защищать свою репродуктивную систему от окислительного стресса и
                                    возрастных заболеваний предстательной железы.

                                </p>

                                <h3 class="title-article after-line">Что делать?</h3>

                                <p>
                                    <b>Давайте говорить начистоту</b>. Есть в ваших планах увеличение семьи или нет – от
                                    работы
                                    репродуктивной системы зависит как объективное самочувствие мужчины, так и его
                                    эмоциональное состояние. Поэтому если вы хотите продлить период активной жизни, то с
                                    35 лет следует уделить больше внимания поддержке репродуктивной системы.
                                    Важная составляющая такой заботы – периодический прием антиоксидантов. Комплекс
                                    <a href="/nashi_produkty/sinergin">«Синергин»</a> предназначен именно для защиты
                                    репродуктивной системы и включает 6
                                    природных антиоксидантов. <br>
                                    Каротиноиды бета-каротин и ликопин, витамины С, Е защищают клетки простаты от
                                    свободных радикалов. Кроме того, ликопин предупреждает или замедляет развитие
                                    гиперплазии простаты. Рутин, витамины С и Е улучшают микроциркуляцию в тканях
                                    предстательной железы, устраняют застойные процессы. Антиоксиданты препятствуют
                                    развитию оксидативного стресса, защищая сосудистую систему полового члена, поражение
                                    которой приводит к эректильной дисфункции. Ряд экспериментальных исследований
                                    показал, что антиоксиданты коэнзим Q10, витамины Е и С оказывают проэректогенные
                                    эффекты, а также значительно усиливают действие таких стимуляторов, как ингибиторы
                                    ФДЭ-5. <br>
                                    Для мужчин, которые планируют пополнение в семье (или рассматривают такую
                                    возможность в некоторой перспективе), немаловажно, что антиоксиданты благоприятно
                                    влияют на качество спермы. Витамин С (вместе с другими антиоксидантами) снижает риск
                                    повреждения генетического материала сперматозоидов. Витамин Е (иногда в сочетании с
                                    другими антиоксидантами) уменьшает количество сперматозоидов с поврежденным
                                    генетическим аппаратом, увеличивает плотность спермы и подвижность сперматозоидов;
                                    кроме того он обладает противовоспалительным действием. Ликопин повышает качество и
                                    количество спермы, количество и подвижность сперматозоидов.
                                    <br>
                                    Многие привыкли думать, что забота о себе – это обязательные спортзал и диета, отказ
                                    от привычек и резкая смена образа жизни. В действительности забота о себе может быть
                                    разной. И прием антиоксидантов, и посещение врача при признаках дискомфорта, и
                                    избегание стрессов – все это компоненты здорового образа жизни, которые не требуют
                                    больших усилий, но при этом помогают дольше чувствовать себя бодрым и энергичным.
                                </p>
