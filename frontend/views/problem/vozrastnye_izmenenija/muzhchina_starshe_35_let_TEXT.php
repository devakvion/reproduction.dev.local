                                            из–за
                                            недостаточного поступления к ней кислорода (ИБС), развитие атеросклероза
                                            (дислипидемия).

                                        </li>
                                        <li>
                                            Сбои в мочеполовой системе: нарушение процесса мочеиспускания (дизурия),
                                            снижение либидо, ускорение семяизвержения (эякуляции) и уменьшение объема
                                            эякулята, невозможность достижения и поддержания эрекции, необходимой для
                                            полового акта (эректильная дисфункция). По данным Массачусетского
                                            исследования
                                            по изучению вопросов старения мужчин (ММАS), более половины мужчин старше 40
