                    <h3 class="title-article after-line">Аргинин </h3>

                    <p>
                        Аминокислота, из которой образуется оксид азота, способствующий расширению сосудов и, как
                        следствие,
                        влияющий на улучшение кровообращения и насыщение тканей кислородом, в том числе в органах малого
                        таза. Это улучшает состояние органов репродуктивной системы и повышает вероятность зачатия.
                    </p>

                    <h3 class="title-article after-line">Витекс (экстракт)</h3>

                    <p>
                        Стимулирует выработку гормона беременности прогестерона и одновременно снижает выделение гормона
                        пролактина. Сочетание высокого уровня пролактина с недостатком прогестерона затрудняет
                        имплантацию
                        оплодотворенной яйцеклетки. Поэтому прием витекса устраняет эту причину возможного ненаступления
                        беременности. <br>
                        Обратите внимание, что экстракт витекса оказывает влияние только на относительно повышенный
                        пролактин. У женщин с нормальным уровнем пролактина не происходит его снижения.

                    </p>

                    <h3 class="title-article after-line">Фолиевая кислота</h3>

                    <p>
                        Необходима для деления и роста клеток, поэтому в период быстрого развития организма (на стадии
                        раннего внутриутробного развития) она требуется в повышенных количествах. Также фолиевая кислота
                        играет важную роль в формировании плаценты и новых кровеносных сосудов в матке. Прием фолиевой
                        кислоты в период подготовки к зачатию и во время беременности снижает риск преждевременного
                        прерывания беременности, кровотечений, развития дефектов нервной трубки у новорожденных.
                    </p>
