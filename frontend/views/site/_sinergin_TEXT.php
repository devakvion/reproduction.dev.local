                    <h3 class="title-article after-line">Что такое оксидативный стресс?</h3>

                    <p>
                        <b>Многие нарушения</b> в работе организма вызваны одним и тем же явлением – оксидативным
                        стрессом.
                        Оксидативный стресс возникает, когда организм долгое время не может справиться с избытком
                        свободных
                        радикалов. <br>
                        Свободные радикалы – это химически активные частицы, у которых есть непарный (свободный)
                        электрон.
                        Они всегда присутствуют в организме, поскольку образуются при протекании многих биохимических
                        реакций. Небольшое количество свободных радикалов не представляет опасности, так как в организме
                        существует антиоксидантная система, регулирующая их численность. <br>
                        Состояние с одним электроном – неестественное для любой частицы. Поэтому свободные радикалы
                        стараются «добыть» пару для своего свободного электрона, оторвав электрон у обычной молекулы. В
                        результате «нормальные» молекулы тоже превращаются в свободные радикалы. В нормальном состоянии
                        «собственные» антиоксиданты организма нейтрализуют свободные радикалы. Но когда свободных
                        радикалов
                        слишком много, антиоксидантная система перестает справляться со свободными радикалами и их
                        количество нарастает лавинообразно. Они могут образовываться как в клетках, так и в межклеточном
                        пространстве. В погоне за электронами свободные радикалы «нападают» на любые молекулы: ДНК,
                        аминокислоты, белки, липиды клеточных стенок и т.д.
                        <img class="dontsplit" src="/img/drugs/oksidativnyj_stress.png" alt="оксидативный стресс">
                    </p>
                </div>

                <div class="not-col dontsplit">
                    <h3 class="title-article after-line">Чем опасен оксидативный стресс?</h3>

                    <p>
                        <b>При оксидативном</b> стрессе в первую очередь страдает репродуктивная система организма.
                        Повреждаются
                        ДНК (генетический материал клеток), липиды, составляющие клеточную стенку, а также истощаются
                        запасы
                        АТФ. Избыток свободных радикалов негативно сказывается на половых клетках (сперматозоидах и
                        яйцеклетках), особенно во время их роста и созревания. Оксидативный стресс связан с многими
                        нарушениями работы репродуктивной системы, такими как мастопатия, простатит, поликистоз
                        яичников,
                        эректильная дисфункция и другие.
                    </p>

                    <p>
                        <b>Оксидативный стресс</b> накапливается с возрастом. Его считают одной из причин старения.
                        Именно
                        он
                        ухудшает состояние мелких и крупных сосудов, ведет к эндокринным проблемам. Поэтому чем дольше
                        живет
                        человек, тем сильнее его организм нуждается в антиоксидантах.
                        <br>
                        Антиоксиданты нейтрализуют свободные радикалы и предотвращают или уменьшают оксидативный стресс.
                        Тем
                        самым устраняется причина развития многих нежелательных состояний и изменений в организме,
                        сохраняется репродуктивное здоровье и продлевается период активной жизни.
                    </p>
