                    <h5 class="title-article after-line">Витамин В<sub>9</sub> (фолаты и фолиевая кислота)</h5>
                    <p>Главная опасность недостатка фолатов при беременности – дефект нервной трубки (ДНТ). Нервная
                        трубка закладывается в первые 28 дней после зачатия.
                        Прием фолатов во время беременности снижает риск нарушения развития мозга, пороков
                        сердечно-сосудистой и мочевыделительной системы, расщелины верхнего неба, дефектов конечностей,
                        больших артерий, пупочной грыжи. Кроме того, фолаты снижают вероятность преждевременных родов на
                        50–70 %, невынашивания, преэклампсии и других проблем.
                    </p>
                </div>
                <div class="not-col dontsplit">
                    <h5 class="title-article after-line">Железо</h5>
                    <p>Железо является основным компонентом гемоглобина – белка, который доставляет кислород во все
                        живые клетки. Во втором триместре беременности кровоток женщины увеличивается примерно на 30 %,
                        а в третьем триместре – на 50 %. Последствия недостатка железа: железодефицитная анемия (ЖДА) у
                        матери и плода, самопроизвольное прерывание беременности, задержка развития, недостаток веса,
                        слабость и вялость, воспалительные процессы.</p>
                </div>
                <div class="not-col dontsplit">
                    <h5 class="title-article after-line">Йод</h5>
                    <p>Йод входит в состав гормонов щитовидной железы, которые, в том числе, активно участвуют в
                        формировании головного мозга. Нехватка йода во время беременности связана с развитием кретинизма
                        у ребенка, а также зоба у беременной. Устранение дефицита йода – обязательное условие нормальных
                        родов.</p>
                </div>
                <div class="not-col dontsplit">
                    <h5 class="title-article after-line">Омега-3</h5>
                    <p>Омега–3 ПНЖК – это эссенциальные (незаменимые) жирные кислоты, которые не синтезируются в
                        организме человека. Жирные кислоты, составляющие мозг человека, на 35 % состоят из ПНЖК, причем
                        25 % приходится на докозагексаеновую кислоту (ДГК). Вот почему Омега-3 остро необходима для
                        формирования головного мозга. Омега-3 также требуется для формирования нервной ткани плода,
                        иммунной системы, обеспечивает рост и созревание плода, формирование органов зрения.
                        <br>
                        Омега-3 увеличивает сроки вынашивания, причем ДГК сокращает риск преждевременных родов,
                        преэклампсии и прерывания беременности, преждевременного отхождения вод при родах.
                    </p>
